package org.s002_data_types;

public class FloatsDemo {
    public static void userMain() {
        float numberFloat = 5.8F; // Single-precision floating point
        double numberDouble = 12589.85; // Double-precision floating point

        System.out.printf("%.2f", numberFloat);
        System.out.println();
        System.out.printf("%.2f", numberDouble);
    }
}
