package org.s003_operators;

public class PrefixPostFixDemo {
    public static void userMain() {
        int a = 10;

        System.out.printf("a = %d\n", a);
        System.out.printf("a++ = %d\n", a++);
        System.out.printf("a = %d\n", a);
        System.out.printf("++a = %d\n", ++a);
        System.out.printf("a = %d\n", a);
    }
}
