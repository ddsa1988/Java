package org.s006_strings;

public class StringLiteralDemo {
    public static void userMain() {
        System.out.println("I like Java".length());
        System.out.println("I like Java".replaceAll(" ", "*"));
        System.out.println("I like Java".charAt(7));
        System.out.println("I like Java".indexOf('a'));
        System.out.println("I like Java".lastIndexOf('a'));
    }
}
